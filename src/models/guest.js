const mongoose = require("mongoose");

const Guest = new mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
        unique: true
    }
})

const Guests = new mongoose.model("GuestNew", Guest);
module.exports = Guests;